def readData(file,sheetName,rownum,columnno):
    wordbook = openpyxl.load_workbook(file)
    sheet = wordbook.get_sheet_by_name(sheetName)
    return sheet.cell(row=rownum,column=columnno).value

def writeData(file,sheetName,rowum,columnno,data):
    wordbook = openpyxl.load_workbook(file)
    sheet = wordbook.get_sheet_by_name(sheetName)
    sheet.cell(row=rowum,column=columnno).value = data
    wordbook.save(file)


def appendData(file, sheetName, rowum, columnno, data):
    wordbook = openpyxl.load_workbook(file)
    sheet = wordbook[sheetName]
    current_value = sheet.cell(row=rowum, column=columnno).value
    if current_value == None:
        sheet.cell(row=rowum, column=columnno).value = data
    else:
        sheet.cell(row=rowum, column=columnno).value = str(current_value) + str(data)
    wordbook.save(file)


